declare(strict_types=1);

namespace LavaMusic\ElasticSearch\Endpoints\Ml;

use LavaMusic\ElasticSearch\Common\Exceptions\RuntimeException;
use LavaMusic\ElasticSearch\Endpoints\AbstractEndpoint;

/**
 * Class Forecast
 * Elasticsearch API name ml.forecast
 */
class Forecast extends AbstractEndpoint
{
    protected $job_id;

    public function getURI(): string
    {
        $job_id = $this->job_id ?? null;

        if (isset($job_id)) {
            return "/_ml/anomaly_detectors/$job_id/_forecast";
        }
        throw new RuntimeException('Missing parameter for the endpoint ml.forecast');
    }

    public function getParamWhitelist(): array
    {
        return [
            'duration',
            'expires_in',
            'max_model_memory'
        ];
    }

    public function getMethod(): string
    {
        return 'POST';
    }

    public function setBody($body): Forecast
    {
        if (isset($body) !== true) {
            return $this;
        }
        $this->body = $body;

        return $this;
    }

    public function setJobId($job_id): Forecast
    {
        if (isset($job_id) !== true) {
            return $this;
        }
        $this->job_id = $job_id;

        return $this;
    }
}